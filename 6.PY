


def print_list(lst):
    """
    Prints out each element of the list.

    Parameters:
    lst (list): The list to be printed.
    """
    for element in lst:
        print(element)

if __name__ == "__main__":
    # Example usage
    sample_list = [1, 2, 3, 4, 5]
    print_list(sample_list)
